'''
Leia uma temperatura em graus Celsius e apresente-a convertira em Graus Kelvin.
A fórmula de conversão é: K = C + 273.15, sendo C a temperatura em Celsius e K
a temperatura em Kelvin.
'''

c = float(input("\nInforme a temperatura em graus Celsius: "))

k = c + 273.15

print("A temperatura informada convertida para graus Kelvin é", k, "\n")
